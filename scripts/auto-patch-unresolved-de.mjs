function translateAggressive(text) {
  if (typeof text !== 'string' || !text.trim()) return null;

  for (const [from, to] of TR_PHRASE_FIXES) {
    if (text === from || text.includes(from)) {
      return text.split(from).join(to);
    }
  }

  for (const [from, to] of PHRASE_FIXES) {
    if (text === from) return to;
  }

  let result = text;
  for (const [from, to] of PHRASE_FIXES) {
    if (result.includes(from)) {
      result = result.split(from).join(to);
    }
  }

  const tokens = result.split(/(\s+|(?=[.,!?;:(){}[\]])|(?<=[.,!?;:(){}[\]]))/);
  result = tokens
    .map((token) => {
      if (!token || /^[\s.,!?;:(){}[\]]$/.test(token)) return token;
      if (/^\{\{[^}]+\}\}$/.test(token)) return token;
      return WORD_MAP.get(token) ?? token;
    })
    .join('');

  if (result !== text && looksGerman(result)) return result;
  if (PHRASE_FIXES.some(([from, to]) => text === from)) {
    const exact = PHRASE_FIXES.find(([from]) => text === from)?.[1];
    if (exact) return exact;
  }
  return null;
}

function main() {
  const unresolved = JSON.parse(fs.readFileSync(UNRESOLVED_PATH, 'utf8'));
  const existingPatch = fs.existsSync(PATCH_PATH)
    ? JSON.parse(fs.readFileSync(PATCH_PATH, 'utf8'))
    : {};
  const patch = { ...existingPatch };
  let added = 0;

  for (const item of unresolved) {
    if (patch[item.key]) continue;
    const source = item.source ?? item.en ?? item.tr;
    const translated = translateAggressive(source);
    if (translated && translated !== item.de && looksGerman(translated)) {
      patch[item.key] = translated;
      added += 1;
    }
  }

  fs.writeFileSync(PATCH_PATH, JSON.stringify(patch, null, 2));
  console.log(`Patch entries: ${Object.keys(patch).length}`);
  console.log(`Added: ${added}`);
}

main();
